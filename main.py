from typing import Union

from fastapi import FastAPI
import sqlite3

connection = sqlite3.connect("workqueue.db")

app = FastAPI()


@app.get("/")
def read_root():
    return {"Hello": "World"}


@app.get("/items/{item_id}")
def read_item(item_id: int, q: Union[str, None] = None):
    return {"item_id": item_id, "q": q}